const HISTORY_KEY_PREFIX = "quizHistory_";
function getHistoryKey() { return HISTORY_KEY_PREFIX + activeExam; }

function saveQuizHistory(score, total) {
    const key = getHistoryKey();
    const existing = JSON.parse(localStorage.getItem(key) || "[]");
    const now = new Date();
    const dateStr = now.toLocaleDateString("th-TH", { day: "numeric", month: "short", year: "numeric" });
    existing.unshift({ score, total, date: dateStr, ts: Date.now() });
    if (existing.length > 20) existing.length = 20;
    localStorage.setItem(key, JSON.stringify(existing));
}

function getScoreClass(score, total) {
    const pct = (score / total) * 100;
    if (pct === 100) return "excellent";
    if (pct >= 80) return "good";
    if (pct >= 60) return "average";
    return "poor";
}

function renderQuizHistory() {
    const panel = document.getElementById("quiz-history-panel");
    if (!panel) return;
    const key = getHistoryKey();
    const history = JSON.parse(localStorage.getItem(key) || "[]");
    if (history.length === 0) {
        panel.innerHTML = '<div class="history-panel-header"><div class="history-panel-title"><i class="fa-solid fa-chart-line"></i> ประวัติผลสอบ</div></div><div class="history-empty"><i class="fa-solid fa-inbox"></i><span>ยังไม่มีประวัติการสอบ ลองทำข้อสอบสักรอบแล้วกลับมาดูสถิติ!</span></div>';
        return;
    }
    const totalAttempts = history.length;
    const avgScore = (history.reduce((s, h) => s + (h.score / h.total * 100), 0) / totalAttempts).toFixed(0);
    const bestScore = history.reduce((best, h) => { const p = h.score/h.total*100; return p > best ? p : best; }, 0).toFixed(0);
    const logItems = history.map(h => {
        const cls = getScoreClass(h.score, h.total);
        const pct = Math.round((h.score / h.total) * 100);
        const label = cls === "excellent" ? "เยี่ยม" : cls === "good" ? "ดี" : cls === "average" ? "พอใช้" : "พลาด";
        return '<div class="history-log-item">'
            + '<span class="history-log-badge ' + cls + '">' + label + '</span>'
            + '<span class="history-log-score">' + h.score + '/' + h.total + '</span>'
            + '<div class="history-log-bar-wrap"><div class="history-log-bar ' + cls + '" style="width:' + pct + '%"></div></div>'
            + '<span class="history-log-date">' + h.date + '</span></div>';
    }).join("");
    panel.innerHTML = '<div class="history-panel-header">'
        + '<div class="history-panel-title"><i class="fa-solid fa-chart-line"></i> ประวัติผลสอบ (' + examData[activeExam].title + ')</div>'
        + '<button class="history-clear-btn" id="history-clear-btn"><i class="fa-solid fa-trash"></i> ล้างประวัติ</button></div>'
        + '<div class="history-stats-row">'
        + '<div class="history-stat-box"><span class="history-stat-num">' + totalAttempts + '</span><span class="history-stat-label">ครั้งที่สอบ</span></div>'
        + '<div class="history-stat-box"><span class="history-stat-num">' + avgScore + '%</span><span class="history-stat-label">คะแนนเฉลี่ย</span></div>'
        + '<div class="history-stat-box"><span class="history-stat-num">' + bestScore + '%</span><span class="history-stat-label">คะแนนสูงสุด</span></div>'
        + '</div><div class="history-log-title">ประวัติล่าสุด</div>'
        + '<div class="history-log">' + logItems + '</div>';
    const clearBtn = document.getElementById("history-clear-btn");
    if (clearBtn) {
        clearBtn.addEventListener("click", () => {
            if (confirm("ล้างประวัติผลสอบทั้งหมดสำหรับ " + examData[activeExam].title + " ?")) {
                localStorage.removeItem(getHistoryKey());
                renderQuizHistory();
            }
        });
    }
}

// Helper for shuffling arrays using Fisher-Yates algorithm
function shuffleArray(array) {
    const arr = [...array];
    for (let i = arr.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [arr[i], arr[j]] = [arr[j], arr[i]];
    }
    return arr;
}

function startQuiz() {
    quizState.started = true;
    quizState.currentIndex = 0;
    quizState.score = 0;
    
    // Get selected subject
    const activeSubjectPill = document.querySelector("#quiz-subject-pills .count-pill.active");
    const selectedSubject = activeSubjectPill ? activeSubjectPill.getAttribute("data-subject") : "all";
    
    // Filter questions based on selected subject
    let originalQuiz = examData[activeExam].quiz;
    if (selectedSubject !== "all") {
        originalQuiz = originalQuiz.filter(q => q.category === selectedSubject);
    }
    
    // Failsafe if no questions match
    if (originalQuiz.length === 0) {
        originalQuiz = examData[activeExam].quiz;
    }
    
    const prefixes = ["ก. ", "ข. ", "ค. ", "ง. "];
    
    const preparedQuestions = originalQuiz.map(q => {
        const correctAnswerText = q.options[q.answer];
        
        // Strip out existing ก. ข. ค. ง. prefixes
        const cleanedOptions = q.options.map(opt => opt.replace(/^[ก-ง]\.\s*/, ""));
        const cleanedCorrectAnswerText = correctAnswerText.replace(/^[ก-ง]\.\s*/, "");
        
        // Shuffle cleaned options
        const shuffledCleaned = shuffleArray(cleanedOptions);
        
        // Find the new index of correct answer
        const newAnswerIndex = shuffledCleaned.indexOf(cleanedCorrectAnswerText);
        
        // Re-apply prefixes ก. ข. ค. ง. based on new order
        const finalOptions = shuffledCleaned.map((opt, idx) => (prefixes[idx] || "") + opt);
        
        // Dynamically correct the explanation prefix to match new answer index
        const newPrefix = prefixes[newAnswerIndex];
        const finalExplanation = q.explanation.replace(/^[ก-ง]\.\s*/, newPrefix);
        
        return {
            question: q.question,
            options: finalOptions,
            answer: newAnswerIndex,
            explanation: finalExplanation
        };
    });
    
    // Get selected question count
    const activePill = document.querySelector("#count-pills .count-pill.active");
    const selectedCount = activePill ? parseInt(activePill.getAttribute("data-count")) : 10;
    
    // Shuffle all questions, then slice to selected count
    const allShuffled = shuffleArray(preparedQuestions);
    quizState.questions = allShuffled.slice(0, Math.min(selectedCount, allShuffled.length));
    quizState.answers = Array(quizState.questions.length).fill(null);
    
    const selectedMode = document.querySelector('input[name="quiz-mode"]:checked');
    quizState.mode = selectedMode ? selectedMode.value : "timed";
    
    document.getElementById("quiz-intro").style.display = "none";
    document.getElementById("quiz-history-panel").style.display = "none";
    document.getElementById("quiz-summary").style.display = "none";
    document.getElementById("quiz-active").style.display = "block";
    
    loadQuestion();
}

function loadQuestion() {
    clearInterval(quizTimer); // Clear any running timer
    
    const activeQuizData = quizState.questions;
    const questionData = activeQuizData[quizState.currentIndex];
    quizState.answered = false;
    
    // Progress calculation
    const progressPercent = (quizState.currentIndex / activeQuizData.length) * 100;
    document.getElementById("quiz-progress").style.width = `${progressPercent}%`;
    
    // Update question metadata
    document.getElementById("question-num-text").textContent = `คำถามข้อที่ ${quizState.currentIndex + 1} จากทั้งหมด ${activeQuizData.length} ข้อ (${examData[activeExam].title})`;
    document.getElementById("question-text-content").innerHTML = questionData.question.replace(/\n/g, "<br>");
    
    const optionsContainer = document.getElementById("options-container");
    optionsContainer.innerHTML = "";
    
    questionData.options.forEach((opt, idx) => {
        const optionCard = document.createElement("div");
        optionCard.className = "option-card";
        optionCard.setAttribute("data-index", idx);
        optionCard.innerHTML = `
            <div class="option-check"></div>
            <div class="option-text">${opt}</div>
        `;
        
        optionCard.addEventListener("click", () => {
            if (quizState.answered) return;
            selectOption(idx);
        });
        
        optionsContainer.appendChild(optionCard);
    });
    
    document.getElementById("quiz-explanation").style.display = "none";
    document.getElementById("quiz-submit-btn").style.display = "block";
    document.getElementById("quiz-submit-btn").disabled = true;
    document.getElementById("quiz-next-btn").style.display = "none";

    // Start countdown timer if in timed mode
    const timerContainer = document.getElementById("quiz-timer-container");
    const timerBar = document.getElementById("quiz-timer-bar");
    const timerText = document.getElementById("quiz-timer-text");
    
    if (quizState.mode === "timed") {
        timerContainer.style.display = "block";
        
        let totalDuration = 30;
        let intervalMs = 100;
        let ticks = totalDuration * (1000 / intervalMs);
        let currentTick = ticks;
        
        timerBar.style.width = "100%";
        timerBar.style.backgroundColor = "var(--success)";
        timerText.style.color = "";
        timerText.innerHTML = `<i class="fa-solid fa-clock"></i> 30 วินาที`;
        
        quizTimer = setInterval(() => {
            if (quizState.answered) {
                clearInterval(quizTimer);
                return;
            }
            
            currentTick--;
            const percentage = (currentTick / ticks) * 100;
            timerBar.style.width = `${percentage}%`;
            
            if (percentage <= 25) {
                timerBar.style.backgroundColor = "var(--error)";
                timerText.style.color = "var(--error)";
            }
            
            const secLeft = Math.ceil(currentTick / (1000 / intervalMs));
            timerText.innerHTML = `<i class="fa-solid fa-clock"></i> ${secLeft} วินาที`;
            
            if (currentTick <= 0) {
                clearInterval(quizTimer);
                timeOut();
            }
        }, intervalMs);
    } else {
        timerContainer.style.display = "none";
        timerText.style.color = "";
        timerText.innerHTML = `<i class="fa-solid fa-circle-info"></i> ไม่จับเวลา`;
    }
}

function selectOption(index) {
    quizState.answers[quizState.currentIndex] = index;
    
    const options = document.querySelectorAll("#options-container .option-card");
    options.forEach(opt => opt.classList.remove("selected"));
    
    const selectedOption = document.querySelector(`#options-container .option-card[data-index="${index}"]`);
    if (selectedOption) {
        selectedOption.classList.add("selected");
    }
    
    document.getElementById("quiz-submit-btn").disabled = false;
}

function submitAnswer() {
    if (quizState.answered) return;
    
    clearInterval(quizTimer);
    const selectedIdx = quizState.answers[quizState.currentIndex];
    const activeQuizData = quizState.questions;
    const qData = activeQuizData[quizState.currentIndex];
    quizState.answered = true;
    
    const isCorrect = selectedIdx === qData.answer;
    if (isCorrect) {
        quizState.score++;
    }
    
    const options = document.querySelectorAll("#options-container .option-card");
    options.forEach((opt, idx) => {
        if (idx === qData.answer) {
            opt.style.borderColor = "var(--success)";
            opt.style.background = "var(--success-bg)";
            opt.querySelector(".option-check").style.background = "var(--success)";
            opt.querySelector(".option-check").style.borderColor = "var(--success)";
        } else if (idx === selectedIdx) {
            opt.style.borderColor = "var(--error)";
            opt.style.background = "var(--error-bg)";
            opt.querySelector(".option-check").style.background = "var(--error)";
            opt.querySelector(".option-check").style.borderColor = "var(--error)";
        }
    });
    
    const explanationPanel = document.getElementById("quiz-explanation");
    explanationPanel.style.display = "block";
    explanationPanel.className = `explanation-panel ${isCorrect ? 'success' : 'error'}`;
    
    const titleText = isCorrect 
        ? "✅ ถูกต้องครับ!" 
        : `❌ ผิดครับ (คำตอบที่ถูกต้องคือ: ${qData.options[qData.answer]})`;
        
    explanationPanel.innerHTML = `
        <div class="explanation-title">${titleText}</div>
        <div class="explanation-text">${qData.explanation}</div>
    `;
    
    document.getElementById("quiz-submit-btn").style.display = "none";
    document.getElementById("quiz-next-btn").style.display = "block";
    document.getElementById("quiz-next-btn").textContent = quizState.currentIndex === activeQuizData.length - 1 
        ? "ดูคะแนนสรุป" 
        : "คำถามถัดไป";
}

function nextQuestion() {
    const activeQuizData = quizState.questions;
    if (quizState.currentIndex < activeQuizData.length - 1) {
        quizState.currentIndex++;
        loadQuestion();
    } else {
        showResults();
    }
}

